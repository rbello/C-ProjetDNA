namespace NetworkComputeFramework.MapReduce
{

    /// <summary>
    /// A data mapper. This interface implements the MapReduce design pattern.
    /// </summary>
    /// <typeparam name="T">Type of the unitary data processed</typeparam>
    public interface IMapper<T> : IDisposable
    {

        long DataLength { get; }

        int ChunkPreferredLength { get; }

        int ChunkRemainsLength { get; }

        int ChunkCount { get; }

        bool Active { get; }

        DataChunk<T> NextChunk();

    }
}
